import { mkdir, readFile, writeFile } from "node:fs/promises";

const rawPath = new URL("../build/BitchTracker.raw.js", import.meta.url);
const outDir = new URL("../dist/", import.meta.url);
const outPath = new URL("../dist/BitchTracker.plugin.js", import.meta.url);

const header = `/**
 * @name BitchTracker
 * @author open-hax
 * @description Shadow-CLJS BetterDiscord plugin scaffold for Discord moderation/event tracking.
 * @version 0.0.1
 */`;

const raw = await readFile(rawPath, "utf8");
const js = raw.replace(/^#!.*\n/, "");
const body = `${header}\n\n// Generated by shadow-cljs. The CLJS entrypoint preserves the BetterDiscord rule:\n// module.exports = meta => ({ start, stop, getSettingsPanel })\n${js.trimEnd()}\n`;

await mkdir(outDir, { recursive: true });
await writeFile(outPath, body);
console.log(`wrote ${outPath.pathname}`);
