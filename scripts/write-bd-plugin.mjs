import { mkdir, readFile, writeFile, copyFile } from "node:fs/promises";
import { build } from "esbuild";
import { homedir } from "node:os";
import { join } from "node:path";

const rawPath = new URL("../build/BitchTracker.raw.js", import.meta.url);
const outDir = new URL("../dist/", import.meta.url);
const outPath = new URL("../dist/BitchTracker.plugin.js", import.meta.url);

const header = `/**
 * @name BitchTracker
 * @author open-hax
 * @description Shadow-CLJS BetterDiscord plugin for OpenPlanner Discord ingestion, reaction labeling, and review backfill.
 * @version 0.0.1
 */`;

const raw = await readFile(rawPath, "utf8");
const js = raw.replace(/^#!.*\n/, "");

await mkdir(outDir, { recursive: true });

const bundled = await build({
  stdin: {
    contents: js,
    resolveDir: new URL("../", import.meta.url).pathname,
    sourcefile: "BitchTracker.raw.js",
    loader: "js",
  },
  bundle: true,
  platform: "browser",
  format: "cjs",
  write: false,
});

if (!bundled.outputFiles?.[0]?.text) {
  throw new Error("esbuild produced no output");
}

const body = `${header}\n\n// Generated by shadow-cljs and bundled with esbuild.\n// The CLJS entrypoint preserves the BetterDiscord rule:\n// module.exports = meta => ({ start, stop, getSettingsPanel, runBackfill, flush })\n${bundled.outputFiles[0].text}`;

const bdCandidates = [
  join(homedir(), ".config", "BetterDiscord", "plugins"),
  join(homedir(), "snap", "discord", "current", ".config", "BetterDiscord", "plugins"),
];

async function copyToBetterDiscord(outPath) {
  for (const dir of bdCandidates) {
    try {
      const target = join(dir, "BitchTracker.plugin.js");
      await copyFile(outPath, target);
      console.log(`copied to ${target}`);
      return;
    } catch (err) {
      if (err.code !== "ENOENT") {
        console.warn(`failed to copy to ${dir}: ${err.message}`);
        return;
      }
    }
  }
  console.warn("BetterDiscord plugins directory not found; skipped copy");
}

await writeFile(outPath, body);
console.log(`wrote ${outPath.pathname}`);
await copyToBetterDiscord(outPath.pathname);
